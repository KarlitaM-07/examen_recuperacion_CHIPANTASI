package sistemas;

public class SocioEsporadico extends Usuario{
	
	private String codigoEspora;
	private String actividadPrefer;
	
	
	public String getCodigoEspora() {
		return codigoEspora;
	}
	public void setCodigoEspora(String codigoEspora) {
		this.codigoEspora = codigoEspora;
	}
	public String getActividadPrefer() {
		return actividadPrefer;
	}
	public void setActividadPrefer(String actividadPrefer) {
		this.actividadPrefer = actividadPrefer;
	}
	

}
